function getCityIndexLetter(cityName) {
  if (!cityName) return '#';
  const trimmed = String(cityName).trim();
  if (!trimmed) return '#';
  const first = trimmed[0];
  if (/[A-Za-z]/.test(first)) return first.toUpperCase();
  if (/[0-9]/.test(first)) return '#';
  return CHAR_LETTER[first] || '#';
}

function compareIndexLetters(a, b) {
  if (a === '#') return 1;
  if (b === '#') return -1;
  return a.localeCompare(b, 'en');
}

